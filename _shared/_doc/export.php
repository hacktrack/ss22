Migrate flags of POP3 user

1.Expecting POP3 account
2.Run export flags tool and store downloaded file
   3.1 Change account type to IMAP 
OR 3.2 Clean account cache
4.Login to account,it is recommended to open all not-synchronized
folders to cache their content
5.Run import flags tool and import stored file(2)
6.You should have your flags just like on original pop3 account

