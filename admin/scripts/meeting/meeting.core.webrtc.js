meeting.core.hashHandler=function()
{
	var data=location.hash.replace('#','').split('&');
	var parameters={};
	for(var i=0; i<data.length; i++)
	{
		if(data.length>0)
		{
			var parsed=data[i].split('=');
			parameters[parsed[0]]=parsed[1];
		}
	}
	
	return parameters;
}

