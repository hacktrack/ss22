meeting.core.parseParams=function(params)
{
	//meeting.console.write('PARAMS:###'+params+'###');
	params=params.split('&');
	var ret={};
	for(var i=0; i<params.length; i++)
	{
		var parts=params[i].split('=');
		if(trim(parts[0])!='')
		{
			ret[urldecode(parts[0])]=urldecode(parts[1]);
		}
	}
	return ret;
}

meeting.core.tryAgainIn=function(callback,timeout,show)
{
	timeout++;
	var gone=0;
	var interval=setInterval(function(){
		gone++;
		if(timeout-gone>0)
		{
			if(show)
			{
				meeting.status.tryingAgain(timeout-gone);
			}
		}
		else
		{
			clearTimeout(interval);
			callback();
		}
	},1000);
	return interval;
}

meeting.core.utf8_encode=function(argString) {
  // http://kevin.vanzonneveld.net
  // +   original by: Webtoolkit.info (http://www.webtoolkit.info/)
  // +   improved by: Kevin van Zonneveld (http://kevin.vanzonneveld.net)
  // +   improved by: sowberry
  // +    tweaked by: Jack
  // +   bugfixed by: Onno Marsman
  // +   improved by: Yves Sucaet
  // +   bugfixed by: Onno Marsman
  // +   bugfixed by: Ulrich
  // +   bugfixed by: Rafal Kukawski
  // +   improved by: kirilloid
  // *     example 1: utf8_encode('Kevin van Zonneveld');
  // *     returns 1: 'Kevin van Zonneveld'

  if (argString === null || typeof argString === "undefined") {
    return "";
  }

  var string = (argString + ''); // .replace(/\r\n/g, "\n").replace(/\r/g, "\n");
  var utftext = '',
    start, end, stringl = 0;

  start = end = 0;
  stringl = string.length;
  for (var n = 0; n < stringl; n++) {
    var c1 = string.charCodeAt(n);
    var enc = null;

    if (c1 < 128) {
      end++;
    } else if (c1 > 127 && c1 < 2048) {
      enc = String.fromCharCode((c1 >> 6) | 192, (c1 & 63) | 128);
    } else {
      enc = String.fromCharCode((c1 >> 12) | 224, ((c1 >> 6) & 63) | 128, (c1 & 63) | 128);
    }
    if (enc !== null) {
      if (end > start) {
        utftext += string.slice(start, end);
      }
      utftext += enc;
      start = end = n + 1;
    }
  }

  if (end > start) {
    utftext += string.slice(start, stringl);
  }

  return utftext;
}


